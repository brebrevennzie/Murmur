export function getInitialStudents(): Student[] {
  const data = safeStorage.getItem('tutor_students_db');
  if (data) {
    try {
      const parsed = JSON.parse(data);
      if (Array.isArray(parsed)) {
        // Migration: If any student has a program with an old ID, remove/reset it
        let modified = false;
        const migrated = parsed.map((student: any) => {
          if (student.program && ['math-egepro', 'math-oge', 'phys-ege', 'eng-b2', 'rus-oge-2026', 'rus-ege-2026', 'lit-ege-2026'].includes(student.program.id)) {
            modified = true;
            return { ...student, program: undefined };
          }
          return student;
        });
        if (modified) {
          safeStorage.setItem('tutor_students_db', JSON.stringify(migrated));
        }
        return migrated;
      }
    } catch (e) {
      console.error('Failed to parse from storage, using initial state', e);
      return INITIAL_STUDENTS;
    }
  }
  // Store default
  safeStorage.setItem('tutor_students_db', JSON.stringify(INITIAL_STUDENTS));
  return INITIAL_STUDENTS;
}

export function saveStudents(students: Student[]) {
  safeStorage.setItem('tutor_students_db', JSON.stringify(students));
}
