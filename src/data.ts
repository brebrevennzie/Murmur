export function normalizeProgram(p: any): SyllabusProgram {
  return {
    id: p.id,
    name: p.name,
    topics: (p.topics || []).map((t: any, idx: number) => {
      if (typeof t === 'string') {
        return {
          id: 't-' + p.id + '-' + idx,
          title: t,
          pdfs: [],
          links: []
        };
      }
      return {
        id: t.id || ('t-' + p.id + '-' + idx),
        title: t.title || '',
        pdfs: t.pdfs || [],
        links: t.links || []
      };
    })
  };
}

export function getInitialPrograms(): SyllabusProgram[] {
  const data = safeStorage.getItem('tutor_syllabus_programs');
  if (data) {
    try {
      const parsed = JSON.parse(data);
      if (Array.isArray(parsed)) {
        // Migration check: if any old program ID exists, or if EGE programs have old/short content, we reset/overwrite to clear the old catalog
        const hasOldPrograms = parsed.some(p => 
          ['math-egepro', 'math-oge', 'phys-ege', 'eng-b2', 'rus-oge-2026', 'rus-ege-2026', 'lit-ege-2026'].includes(p.id) ||
          (p.id === 'rus-ege-75h' && (p.topics || []).length < 75) ||
          (p.id === 'rus-ege-38h' && (p.topics || []).length < 38)
        );
        if (!hasOldPrograms) {
          return parsed.map(normalizeProgram);
        }
      }
    } catch (e) {
      console.error('Failed to parse tutor_syllabus_programs, using default template', e);
    }
  }
  const normalized = DEFAULT_PROGRAMS.map(normalizeProgram);
  safeStorage.setItem('tutor_syllabus_programs', JSON.stringify(normalized));
  return normalized;
}

export function savePrograms(programs: SyllabusProgram[]) {
  safeStorage.setItem('tutor_syllabus_programs', JSON.stringify(programs));
}

